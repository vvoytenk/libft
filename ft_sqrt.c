int	ft_sqrt(int nb)
{
	int	sqrt;

	if (nb == 4)
		return (2);
	else
	{
		sqrt = nb / 4;
		while (sqrt >= 1)
		{
			if (sqrt * sqrt == nb)
				return (sqrt);
			sqrt--;
		}
		return (0);
	}
}
